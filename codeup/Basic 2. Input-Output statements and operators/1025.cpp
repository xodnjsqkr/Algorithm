#include <iostream>
using namespace std;
int main(void) 
{
    int a,b,c,d,e;
    scanf("%1d%1d%1d%1d%1d", &a, &b, &c, &d, &e);
    cout<<"["<<a*10000<<"]"<<"\n";
    cout<<"["<<b*1000<<"]"<<"\n";
    cout<<"["<<c*100<<"]"<<"\n";
    cout<<"["<<d*10<<"]"<<"\n";
    cout<<"["<<e<<"]";
    return 0;
}